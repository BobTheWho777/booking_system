-- Добавляем индекс для предотвращения двойных бронирований
-- Этот индекс обеспечит уникальность комбинации room_id + период времени
CREATE UNIQUE INDEX IF NOT EXISTS idx_unique_room_booking 
ON bookings(room_id, check_in, check_out);

-- Примечание: Для полной защиты от double booking нужно изменить логику на уровне приложения
-- или использовать триггеры. Данный индекс поможет выявить дубликаты при вставке.
